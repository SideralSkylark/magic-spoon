    // JavaScript to make spans disappear on click
    document.addEventListener('DOMContentLoaded', function () {
        const spans = document.querySelectorAll('.cereal_plot span');

        spans.forEach(function (span) {
            span.addEventListener('click', function () {
                this.style.display = 'none';
            });
        });
    });

    document.addEventListener('DOMContentLoaded', function () {
        const spans = document.querySelectorAll('.cereal_plot span');

        spans.forEach(function (span) {
            // Generate random top and left positions within the viewport
            const randomTop = Math.floor(Math.random() * window.innerHeight);
            const randomLeft = Math.floor(Math.random() * window.innerWidth);

            // Set the top and left positions
            span.style.top = randomTop + 'px';
            span.style.left = randomLeft + 'px';

            span.addEventListener('click', function () {
                this.style.display = 'none'; // Hide the span when clicked
            });
        });
    });
</script>

